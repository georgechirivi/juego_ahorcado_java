public class Arreglo {
    public static void main(String[] args) throws Exception {
        //Arreglos: estructuras de datos que tenen valores del mismo tipo bajo un mismo nombre
        //Estos valores se distribuyen secuencialmente en la memoria y se puede acceder a ellos mediante indice
        //Los arreglos son tambien llamados vectores o matrices
        //Dato importante el indice se empieza a contar por cero y no por uno
        int[] numero = new int [5];
        numero[0] =1;
        numero[1] =2;
        numero[2] =3;
        numero[3] =4;

        System.err.println(numero);

}
}